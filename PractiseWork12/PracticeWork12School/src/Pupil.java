public class Pupil {

    private String name;
    private int age;
    private String klass;

    public Pupil(String name, int age, String klass) {
        this.name = name;
        this.age = age;
        this.klass = klass;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public String getKlass() {
        return klass;
    }
}
